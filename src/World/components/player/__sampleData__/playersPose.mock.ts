import { RawPoseEvents, RawPoseTypes } from "../animations/Pose.model";

export function getAllPlayerPoses(): RawPoseEvents[][] {
  return Object.values(poses).map((data) => data.map((p) => convertPoses(p)));
}

// playerIdx between 0-10
export function getPlayerPoses(
  teamIdx: number,
  playerIdx: number
): RawPoseEvents {
  return convertPoses(poses[teamIdx][playerIdx]);
}

function convertPoses(poses: (number | string)[]) {
  const result: RawPoseEvents = {};
  for (let index = 0; index < poses.length; index += 2) {
    result[poses[index] as number] = poses[index + 1] as RawPoseTypes;
  }

  return result;
}

